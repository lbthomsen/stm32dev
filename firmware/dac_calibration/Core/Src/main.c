// Send printf to uart1
int _write(int fd, char *ptr, int len) {
    HAL_StatusTypeDef hstatus;

    if (fd == 1 || fd == 2) {
        hstatus = HAL_UART_Transmit(&huart1, (uint8_t*) ptr, len, HAL_MAX_DELAY);
        if (hstatus == HAL_OK)
            return len;
        else
            return -1;
    }
    return -1;
}

void dac_set_value(const uint32_t channel, uint32_t value) {
    HAL_DAC_SetValue(&hdac, channel, DAC_ALIGN_12B_R, value);
    HAL_DAC_Start(&hdac, channel);
}

void dac_set_voltage(const uint32_t channel, float value) {
    dac_set_value(channel, (uint32_t)round(dac_correction * value * dac_multiplier));
}

uint16_t adc_get_value(const uint32_t channel) {

    //uint32_t start_time = HAL_GetTick();

    ADC_ChannelConfTypeDef sConfig = { 0 };
    sConfig.Channel = channel;
    sConfig.Rank = 1;
    sConfig.SamplingTime = ADC_SAMPLETIME_56CYCLES;

    if (HAL_ADC_ConfigChannel(&hadc1, &sConfig) != HAL_OK) {
        Error_Handler();
    }

    //DBG("Starting convertion");

    if (HAL_ADC_Start(&hadc1) != HAL_OK) {
        DBG("ADC Error");
        Error_Handler();
    }

    if (HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY) != HAL_OK) {
        DBG("ADC Poll Error");
        Error_Handler();
    }

    //DBG("Conversion done - time = %lu", HAL_GetTick() - start_time);

    return HAL_ADC_GetValue(&hadc1);

}

float adc_get_voltage(uint16_t raw) {
    return (float)raw * adc_multiplier;
}

